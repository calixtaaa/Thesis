-- Products + transactions for the vending machine bridge (sync/machine_supabase_bridge.py).
-- Run once in Supabase → SQL Editor (adjust names only if you already diverged).

create table if not exists public.products (
  id bigint generated by default as identity primary key,
  slot_number integer not null unique,
  name text not null,
  details text,
  price numeric not null,
  capacity integer not null,
  current_stock integer not null,
  updated_at timestamptz not null default now()
);

create index if not exists products_slot_number_idx on public.products (slot_number);

create table if not exists public.transactions (
  id bigint generated by default as identity primary key,
  -- Stable id from the machine's local SQLite `transactions.id` (enables safe upserts / replays)
  source_tx_id bigint unique,
  created_at timestamptz not null default now(),
  product_id bigint,
  product_name text,
  slot_number integer,
  quantity integer,
  total_amount numeric,
  payment_method text,
  rfid_user_id bigint
);

create index if not exists transactions_created_at_idx on public.transactions (created_at desc);
create unique index if not exists transactions_source_tx_id_ux on public.transactions (source_tx_id);

-- Backwards-compatible migration (if the table already exists)
alter table public.transactions
  add column if not exists source_tx_id bigint;
create unique index if not exists transactions_source_tx_id_ux on public.transactions (source_tx_id);

-- ---------- Automatic duplicate cleanup ----------
-- If duplicates were inserted before `source_tx_id` existed (or before the index was added),
-- this trigger removes duplicates as new rows arrive. It keeps the *earliest* row per key.
-- Key priority:
--   1) source_tx_id (best, stable from the machine)
--   2) fallback "fingerprint" based on the main sale fields (only when source_tx_id is NULL)

create or replace function public.tr_dedupe_transactions()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  keep_id bigint;
begin
  if new.source_tx_id is not null then
    select min(id) into keep_id from public.transactions where source_tx_id = new.source_tx_id;
    delete from public.transactions
    where source_tx_id = new.source_tx_id
      and id <> keep_id;
    return new;
  end if;

  -- Fallback fingerprint (only for rows without source_tx_id)
  select min(id) into keep_id
  from public.transactions
  where source_tx_id is null
    and created_at = new.created_at
    and product_id is not distinct from new.product_id
    and product_name is not distinct from new.product_name
    and slot_number is not distinct from new.slot_number
    and quantity is not distinct from new.quantity
    and total_amount is not distinct from new.total_amount
    and payment_method is not distinct from new.payment_method
    and rfid_user_id is not distinct from new.rfid_user_id;

  delete from public.transactions
  where source_tx_id is null
    and created_at = new.created_at
    and product_id is not distinct from new.product_id
    and product_name is not distinct from new.product_name
    and slot_number is not distinct from new.slot_number
    and quantity is not distinct from new.quantity
    and total_amount is not distinct from new.total_amount
    and payment_method is not distinct from new.payment_method
    and rfid_user_id is not distinct from new.rfid_user_id
    and id <> keep_id;

  return new;
end;
$$;

drop trigger if exists tr_transactions_dedupe on public.transactions;
create trigger tr_transactions_dedupe
  after insert on public.transactions
  for each row
  execute function public.tr_dedupe_transactions();

