-- ---------- Optional: live_feed dedupe (prevents repeated feed spam) ----------
-- Requires public.live_feed to exist (run WebSite/supabase/machine_live_feed.sql first).
-- Dedupes by payload.source_tx_id (best) or payload.transaction_id; otherwise uses a fallback fingerprint.

create or replace function public.tr_dedupe_live_feed()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  keep_id bigint;
  k text;
begin
  -- Prefer stable transaction id from payload.
  k := nullif(trim(coalesce(new.payload->>'source_tx_id', new.payload->>'transaction_id', '')), '');
  if k is not null then
    select min(id) into keep_id
    from public.live_feed
    where coalesce(payload->>'source_tx_id', payload->>'transaction_id') = k;

    delete from public.live_feed
    where coalesce(payload->>'source_tx_id', payload->>'transaction_id') = k
      and id <> keep_id;
    return new;
  end if;

  -- Fallback fingerprint (only when payload doesn't include a stable transaction id).
  select min(id) into keep_id
  from public.live_feed
  where created_at = new.created_at
    and event_type is not distinct from new.event_type
    and message is not distinct from new.message
    and quantity is not distinct from new.quantity
    and total_amount is not distinct from new.total_amount;

  delete from public.live_feed
  where created_at = new.created_at
    and event_type is not distinct from new.event_type
    and message is not distinct from new.message
    and quantity is not distinct from new.quantity
    and total_amount is not distinct from new.total_amount
    and id <> keep_id;

  return new;
end;
$$;

do $$
begin
  if to_regclass('public.live_feed') is not null then
    drop trigger if exists tr_live_feed_dedupe on public.live_feed;
    create trigger tr_live_feed_dedupe
      after insert on public.live_feed
      for each row
      execute function public.tr_dedupe_live_feed();
  end if;
end $$;
